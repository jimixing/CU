package com.ebao.gs.sp.uiconfig;

import com.vaadin.ui.Button;
import com.vaadin.ui.Label;
import com.vaadin.ui.NativeButton;
import com.vaadin.ui.Panel;

public class ComponentFactory {
	
	public static Button createDefaultButton(String caption){
			NativeButton button = new NativeButton(caption);
			button.addStyleName("default-button");
			return button; 
	}
	
	public static Panel createDefaultPanel(String caption){
		Panel panel=new Panel(caption);
//		panel.addStyleName("default-panel");
		
		return panel;
	}
	
	public static Label createDefaultLabel(String caption){
		Label label=new Label(caption);
		label.addStyleName("default-label");
		return label;
		
	}
	
	

}

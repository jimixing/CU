package com.ebao.gs.pol.prdt.config;

import java.util.LinkedList;
import java.util.List;

public class SkinboInfo {
	private int NodeID;
	private String nodeName;
	private String nodeDisplayName;
	
	private String className;
	
	private List<SkinboInfo> embedSkinBo=new LinkedList<SkinboInfo>();
	private List<FieldInfo> fieldList=new LinkedList<FieldInfo>();


	public List<FieldInfo> getFieldList() {
		return fieldList;
	}

	public void setFieldList(List<FieldInfo> fieldList) {
		this.fieldList = fieldList;
	}

	public int getNodeID() {
		return NodeID;
	}

	public void setNodeID(int nodeID) {
		NodeID = nodeID;
	}

	public String getNodeName() {
		return nodeName;
	}

	public void setNodeName(String nodeName) {
		this.nodeName = nodeName;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public List<SkinboInfo> getEmbedSkinBo() {
		return embedSkinBo;
	}

	public void setEmbedSkinBo(List<SkinboInfo> embedSkinBo) {
		this.embedSkinBo = embedSkinBo;
	}

	public String getNodeDisplayName() {
		return nodeDisplayName;
	}

	public void setNodeDisplayName(String nodeDisplayName) {
		this.nodeDisplayName = nodeDisplayName;
	}
	
	
	

}
